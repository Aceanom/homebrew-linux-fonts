  sha256 "4f12c44954344602370a63700b954766e588542dedcdd702fc65457701ec22b9"

  def install
    (share/"fonts").install Dir.glob("./**/IosevkaSS14.ttc")[0]
  end

  test do
    assert_path_exists share/"fonts"
  end
end
